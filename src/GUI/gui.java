    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        fileChooser.setFileFilter(new FileFilterHtm());

        int returnVal = fileChooser.showSaveDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            if (file.exists()) {
                int ans = JOptionPane.showConfirmDialog(this,
                        "Do you want to overwrite the existing file?", "A file with that name already exists!",
                        JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);

                while (ans == 1) {
                    returnVal = fileChooser.showSaveDialog(this);
                    if (returnVal == JFileChooser.APPROVE_OPTION) {
                        file = fileChooser.getSelectedFile();
                        if (file.exists()) {
                            ans = JOptionPane.showConfirmDialog(this,
                                    "Do you want to overwrite the existing file?", "A file with that name already exists!",
                                    JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                }
                if (ans == 0) {
                    try {
                        Report.saveReport(file.getAbsolutePath());
                        statusBar.setText("File saved to "+file.getAbsolutePath());
//                        fileChooser.setCurrentDirectory(file); //Tror den gjør dette automatisk
                    } catch (IOException ex) {
                        statusBar.setText("Problem saving file" + file.getAbsolutePath());
                    }
                }
            } else {
                try {
                    Report.saveReport(file.getAbsolutePath());
                    statusBar.setText("File saved to "+file.getAbsolutePath());
                } catch (IOException ex) {
                    statusBar.setText("Problem saving file" + file.getAbsolutePath());
                }
            }
        } else {
            statusBar.setText("Save command cancelled by user");
        }


    }//GEN-LAST:event_jButton5ActionPerformed

