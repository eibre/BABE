    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        statusBar.setText("Calculating...");
        jButton1.setEnabled(false);

        SwingWorker worker = new SwingWorker<String, Void>() {

            String status = null;

            @Override
            protected String doInBackground() throws Exception {
                try {
                    //Kjører iterasjonsmetoden og oppdaterer tabellen
                    String[][] table = IterationMethod.iteration();
                    String[] header = {"Layer", "Reinforcement", "Stress", "UR Stress", "Strain", "UR Strain"};
                    TableModel model = new DefaultTableModel(table, header);
                    jTable1.setModel(model);

                    //kjører skjærkontroll og oppdaterer jLabel
                    double[] ur = calculation.Shear.utilization();
                    DecimalFormat df = new DecimalFormat("#");
                    DecimalFormat dfPercent = new DecimalFormat("#.0 %");

                    if (jCheckBoxShear.isSelected()) {
                        String shearRes = "<html>Shear control:<br> <table>"
                            + "<tr><td>Angle:</td><td>" + df.format(ur[0]) + "</td></tr>"
                            + "<tr><td>Shear force:</td><td>" + df.format(ur[1]) + "</td></tr>"
                            + "<tr><td>Shear capacity:</td><td>" + df.format(ur[2]) + "</td></tr>"
                            + "<tr><td>Utilzation ratio:</td><td>" + dfPercent.format(ur[3]) + "</td></tr></table>";
                    jLabelShear.setText(shearRes);
                    }

                    //oppdaterer reultatfanen

                    Report.writeResultArea(true, true, true, true, jCheckBoxShear.isSelected(), true);
                    status = "Calculation done! More details in report tab [alt+6]";
                    //sender melding til statusbar.


                } catch (calculation.IterationException ie) {
                    status = ie.getMessage();
                    JOptionPane.showMessageDialog(resultTab, ie.getMessage(), "Warning", JOptionPane.WARNING_MESSAGE);
                }
                return status;
            }

            @Override
            protected void done() {
                jButton1.setEnabled(true);
                statusBar.setText(status);
            }
        };
        worker.execute();
        
    }//GEN-LAST:event_jButton1ActionPerformed

