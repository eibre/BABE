    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // Script som endrer teksten i jTextFieldFck etter valg i ComboBox

        // Definerer datatyper
        int SelectedIndex;
        String concreteClass;
        String fckStr, fctmStr, EcmStr, epsCuStr, epsCStr;

        double fck;
        double alphacc;
        double gammac;


        // Henter data fra GUI
        SelectedIndex = jComboBox1.getSelectedIndex(); //henter valg nr
        concreteClass = jComboBox1.getItemAt(SelectedIndex).toString(); //får ut objektet som gjøres om til string
        fckStr = concreteClass.replace("B", ""); //fjerner B foran fastheten
        jTextFieldFck.setText(fckStr); // endrer teksten i jTextFieldFck

        fck = Double.parseDouble(fckStr);
        alphacc = Double.parseDouble(jTextFieldAcc.getText());
        gammac = Double.parseDouble(jTextFieldGc.getText());

        //henter resten av data fra klassen Materialer
        double[] material = calculation.Materials.strengthConcrete(fck, alphacc, gammac);

        Locale loc = new Locale("en", "US");
        NumberFormat nf = NumberFormat.getNumberInstance(loc);
        DecimalFormat oneDec = (DecimalFormat) nf;
        DecimalFormat twoDec = (DecimalFormat) nf;
        oneDec.applyPattern("##.#");
        twoDec.applyPattern("##.##");
        DecimalFormat nullDec = new DecimalFormat("###");

        EcmStr = nullDec.format(material[0]);
        fctmStr = twoDec.format(material[4]);
        epsCuStr = oneDec.format(material[2] * -1000);
        epsCStr = oneDec.format(material[1] * -1000);

        //Endrer feltene i GUI
        jTextFieldEcm.setText(EcmStr);
        jTextFieldFctm.setText(fctmStr);
        jTextFieldEpsCu.setText(epsCuStr);
        jTextFieldEpsC.setText(epsCStr);

}//GEN-LAST:event_jComboBox1ActionPerformed

